package pe.jcpr.practica1.dto;

public class practica1Dto {

	private int horasdiarias;
	private int  diaslab;
	private double pagoxhora;
	private double reten;
	private double total;
	
	public double getReten() {
		return reten;
	}
	public void setReten(double reten) {
		this.reten = reten;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	public int getHorasdiarias() {
		return horasdiarias;
	}
	public void setHorasdiarias(int horasdiarias) {
		this.horasdiarias = horasdiarias;
	}
	public int getDiaslab() {
		return diaslab;
	}
	public void setDiaslab(int diaslab) {
		this.diaslab = diaslab;
	}
	public double getPagoxhora() {
		return pagoxhora;
	}
	public void setPagoxhora(double pagoxhora) {
		this.pagoxhora = pagoxhora;
	}
	
	
}
